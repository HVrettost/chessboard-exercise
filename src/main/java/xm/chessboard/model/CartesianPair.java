package xm.chessboard.model;

public class CartesianPair {

    private final int i;
    private final int j;

    public CartesianPair(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }
}


